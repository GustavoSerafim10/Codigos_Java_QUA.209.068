public class Atividade3 {

	public static void main(String[] args) {
		// Faça um programa que imprima na tela apenas os números ímpares entre 1 e 50.
		
		  System.out.println("Números ímpares de 1 a 50:");
	        for (int i = 1; i <= 50; i++) {
	            if (i % 2 != 0) {
	                System.out.print(i + " ");
	            }
	        }

	}

}
